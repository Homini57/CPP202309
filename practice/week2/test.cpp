#include <iostream>

using namespace std;

int main()
{
	// 정수 변수 정의 및 초기화
	int a = 10;
	int b = 15;

	// cout : c++ 출력 함수
	// endl;를 적음으로써 cout 종료
	cout << a + b << endl;

	char text1[10] = "Hello";
	char text2[50] = "world";

	cout << text1 << "!" << endl;
	cout << text2 << "!" << endl;

	return 0;

}